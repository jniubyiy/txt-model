# Прямое отображение: символ (str длины 1) -> соответствующий байт (0..255)
SYMBOL_TO_BYTE = {chr(i): i for i in range(256)}

# Обратное отображение: байт (0..255) -> символ (str длины 1)
BYTE_TO_SYMBOL = {i: chr(i) for i in range(256)}

